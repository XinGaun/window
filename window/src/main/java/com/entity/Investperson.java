package com.entity;
/**
 * 投资客户表实体类
 * @author 74128
 *
 */
public class Investperson {
	private int investpersonid;
	private String investpersonphoto;
	private String investpersonpass;
	public int getInvestpersonid() {
		return investpersonid;
	}
	public void setInvestpersonid(int investpersonid) {
		this.investpersonid = investpersonid;
	}
	public String getInvestpersonphoto() {
		return investpersonphoto;
	}
	public void setInvestpersonphoto(String investpersonphoto) {
		this.investpersonphoto = investpersonphoto;
	}
	public String getInvestpersonpass() {
		return investpersonpass;
	}
	public void setInvestpersonpass(String investpersonpass) {
		this.investpersonpass = investpersonpass;
	}
	
}
